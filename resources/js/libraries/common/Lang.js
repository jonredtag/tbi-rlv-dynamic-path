    trans(hash, data = null) {
        // return hash;
        const commands = hash.split('.');

        const langData = this.data[window.Locale];

        if (
            Object.prototype.hasOwnProperty.call(langData, commands[0]) &&
            Object.prototype.hasOwnProperty.call(langData[commands[0]], commands[1])
        ) {
            let s = langData[commands[0]][commands[1]];

            // If data object is set - use key/values and replace the key in the language string
            if (data !== null) {
                for (const key in data) {
                    s = s.replace(new RegExp(`:${key}`, 'i'), data[key]);
                }
            }

            // return `[${s}]`;
            return s;
        }
        console.log(hash, 'lang entry not found');

        return hash;
    }

